#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*d;

	d = dst;
	while (len--)
		if (*src)
			*dst++ = *src++;
		else
			*dst++ = 0;
	return (d);
}
