#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;
	size_t	i;

	len = ft_strlen(s);
	i = 0;
	while (i <= len)
		if (s[len - i] == c)
			return ((char*)s + len - i);
		else
			i++;
	return (NULL);
}
